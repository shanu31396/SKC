//BASIC I/O 
#include<iostream>
using namespace std;
int main(){
    int num1, num2;

    // << is insertion operator
    cout<<"Enter the value of num1 :"<<endl;

    // >> is extraction operator
    cin>>num1;

    cout<<"Enter the value of num2 :"<<endl;
    cin>>num2;

    cout<<"The sum of num1 and num2 is : "<<num1+num2;
    
    return 0;
}
    
